// Helper functions for date handling
export const getAvailableDates = (cityName) => {
  if (!weatherDatasets[cityName]) return [];
  return weatherDatasets[cityName].days.map((day) => day.datetime);
};

export const isDateAvailable = (cityName, date) => {
  if (!weatherDatasets[cityName]) return false;
  const formattedDate = formatDate(date);
  return weatherDatasets[cityName].days.some(
    (day) => day.datetime === formattedDate
  );
};

